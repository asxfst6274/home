const mongoose = require("mongoose");

const Schema = mongoose.Schema;

const kycSchema = new Schema(
  {
    type: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    status: {
      type: String,
      default: "pending",
      required: true,
    },
    user: {
      type: Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("Kyc", kycSchema);
